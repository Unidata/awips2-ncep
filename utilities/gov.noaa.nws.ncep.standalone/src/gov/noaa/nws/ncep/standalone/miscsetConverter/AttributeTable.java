/**
 * AttributeTable
 * 
 * Class which defines which attribute files need to be generated,
 * and for each, its required relative path and file name, content,
 * order, and for each attribute it contains, a default value, and
 * (if applicable) where the corresponding value can be found in
 * the legacy table. 
 * 
 * <pre>
 * SOFTWARE HISTORY
 * Date         Ticket#     Engineer    Description
 * ------------ ----------  ----------- --------------------------
 * 20 Jul 2011  397         B. Hebbard  Initial creation
 * </pre>
 * 
 * @author B. Hebbard
 * @version 1
 */
